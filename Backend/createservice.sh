FROM node:19-alpine

WORKDIR /usr/src/app/common
COPY common/package*.json .
RUN npm ci --only=production
COPY common/. .

WORKDIR /usr/src/app/$SERVICE_NAME
COPY $SERVICE_NAME/package*.json .
RUN npm ci --only=production
COPY $SERVICE_NAME/. .

RUN npm run build

