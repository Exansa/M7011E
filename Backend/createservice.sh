#!/bin/bash

# This script creates a Node service.
# It takes the name of the service as an argument.
# The service is Node.js based and uses Express.js as the framework.

# Make sure parameter is passed
if [[ -z $1 ]]; then 
    echo "$(tput setaf 1)No parameter passed"
    exit -1
fi
SERVICE_NAME=$(echo $1 | tr '[:upper:]' '[:lower:]')

# Check if node is installed
if ! command -v node &> /dev/null; then
    echo "$(tput setaf 1)node could not be found. Please install node and try again."
    exit -1
fi

# Create folder
if [[ -d "./$SERVICE_NAME" ]]; then
    echo "$(tput setaf 1)$SERVICE_NAME already exists on your filesystem."
    exit -1
fi
mkdir $SERVICE_NAME

# Initialize npm project
cd $SERVICE_NAME

npm init -y
npm i express amqplib uuid
npm i typescript ts-node nodemon @types/node @types/express eslint @typescript-eslint/parser @typescript-eslint/eslint-plugin prettier eslint-config-prettier eslint-plugin-prettier --save-dev
mkdir src

# Create boilerplate files
cat > src/index.ts << EOF
'use strict';

import express from 'express';
import Rabbitmq from '../../common/rabbitmq';

const app: express.Application = express();

const PORT = process.env.PORT ?? 8080;

const rabbitmq = new Rabbitmq();
rabbitmq.listen('$SERVICE_NAME', async (message) => {
	return { success: true, response: message.content.toString() };
});

app.get('/healthcheck', (_req, _res) => {
	_res.send({ status: 'ok' });
});

// Server setup
app.listen(PORT, () => {
	console.info(`Started service $SERVICE_NAME`);
});

EOF

cat > tsconfig.json << EOF
{
    "compilerOptions": {
        "target": "es6",
		"module": "commonjs",
		"rootDirs": ["./src", "../common"],
		"outDir": "./build",
		"esModuleInterop": true,
		"moduleResolution": "node",
		"strict": true,
		"forceConsistentCasingInFileNames": true
    }
}
EOF

# Add scripts to package.json
awk -v service_name=$SERVICE_NAME '/scripts/ { print; print "\t\t\"start:dev\": \"nodemon src/index.ts\",\n\t\t\"start\": \"node ./build/"service_name"/src/index.js\",\n\t\t\"build\": \"tsc\","; next }1' package.json > tmp.package.json
mv tmp.package.json package.json

